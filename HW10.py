"""Реализовать класс Matrix (матрица). Обеспечить перегрузку конструктора класса (метод
__init__()), который должен принимать данные (список списков) для формирования матрицы.
Подсказка: матрица — система некоторых математических величин, расположенных в виде
прямоугольной схемы. Следующий шаг — реализовать перегрузку метода __str__()
для вывода матрицы в привычном виде.
Далее реализовать перегрузку метода __add__() для сложения двух объектов класса Matrix
(двух матриц). Результатом сложения должна быть новая матрица.
Подсказка: сложение элементов матриц выполнять поэлементно.
Первый элемент первой строки первой матрицы складываем с
первым элементом первой строки второй матрицы и пр."""


class Matrix:
    def __init__(self, matrix):
        self.matrix = matrix

    def __str__(self):
        return str('\n'.join([' '.join([str(el) for el in i]) for i in self.matrix]))

    def __add__(self, other):
        if len(self.matrix) == len(other.matrix):
            for i in range(len(self.matrix)):
                for j in range(len(other.matrix[i])):
                    self.matrix[i][j] = self.matrix[i][j] + other.matrix[i][j]
            return Matrix.__str__(self)
        else:
            print('Matrices not match')


mat1 = Matrix([[1, 0, 3], [2, 0, 1], [0, 1, 6], [1, 1, 3]])
mat2 = Matrix([[3, 0, 2], [5, 0, 2], [0, 2, 1], [2, 2, 3]])
print(mat1.__add__(mat2))

